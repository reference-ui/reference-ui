/**
 * Emits `src/icons/index.ts` with explicit re-exports of icon components from
 * `@reference-ui/icons` so bundlers can tree-shake `@reference-ui/lib/icons`.
 *
 * Reads `packages/reference-icons/src/generated/index.ts` (same surface as
 * `@reference-ui/icons`, which only re-exports generated icons).
 */

import { readFile, writeFile } from 'node:fs/promises'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const packageRoot = join(dirname(fileURLToPath(import.meta.url)), '..')
const iconsPackageRoot = join(packageRoot, '../reference-icons')
const iconsGeneratedIndex = join(iconsPackageRoot, 'src/generated/index.ts')
const outFile = join(packageRoot, 'src/icons/index.ts')

const fromIconsPkg = "from '@reference-ui/icons'"

function parseGeneratedExport(line) {
  const m = /^\s*export\s*\{\s*([^}]+)\s*\}\s*from\s*'\.\/[^']+'\s*;?\s*$/.exec(line)
  if (!m) return null
  const names = m[1].trim()
  return `export { ${names} } ${fromIconsPkg}`
}

async function main() {
  const genSrc = await readFile(iconsGeneratedIndex, 'utf8')
  const iconLines = []
  for (const line of genSrc.split('\n')) {
    const t = line.trim()
    if (t === '' || t.startsWith('/**') || t.startsWith('*') || t.startsWith('*/')) continue
    const out = parseGeneratedExport(line)
    if (!out) {
      if (t === '') continue
      throw new Error(`Unexpected line in generated index: ${line}`)
    }
    iconLines.push(out)
  }

  if (iconLines.length === 0) {
    throw new Error('No icon re-exports parsed from reference-icons generated index')
  }

  const header = [
    '/**',
    ' * Generated by scripts/generate-icons-barrel.mjs — do not edit.',
    ' * Mirrors `@reference-ui/icons` (generated icon components only).',
    ' */',
    '',
  ]

  const body = iconLines.join('\n') + '\n'
  await writeFile(outFile, header.join('\n') + body, 'utf8')
  console.error(`Wrote ${outFile} (${iconLines.length} icon re-exports).`)
}

await main()
