import { readFileSync } from 'node:fs'
import { join } from 'node:path'

function readTags(tagsPath: string): string[] {
  const tagsContent = readFileSync(tagsPath, 'utf8')
  const listStart = tagsContent.indexOf('[')
  const listEnd = tagsContent.indexOf('] as const', listStart)
  if (listStart < 0 || listEnd < 0) {
    throw new Error('Could not parse TAGS from system/primitives/tags.ts')
  }

  return tagsContent
    .slice(listStart + 1, listEnd)
    .split(',')
    .map((value) => value.replace(/['"]/g, '').trim())
    .filter(Boolean)
}

function toExportName(tag: string): string {
  if (tag === 'object') return 'Obj'
  if (tag === 'var') return 'Var'
  if (tag.length <= 1) return tag.toUpperCase()
  return tag.charAt(0).toUpperCase() + tag.slice(1)
}

function generatePrimitive(tag: string): string {
  const exportName = toExportName(tag)
  const primitiveClass = `ref-${tag}`

  return [
    `export const ${exportName} = React.forwardRef<PrimitiveElement<'${tag}'>, PrimitiveProps<'${tag}'>>(`,
    `(allProps, ref) => { `,
    `const typedProps = allProps as PrimitiveProps<'${tag}'> & Record<string, unknown>; `,
    `const { className, children, colorMode, styleProps, elementProps } = splitPrimitiveProps(typedProps); `,
    `const inheritsLayerScope = React.useContext(LayerScopeContext); `,
    `const inheritedColorMode = React.useContext(ColorModeContext); `,
    `const hasExplicitColorMode = colorMode != null && colorMode !== ''; `,
    `const resolvedColorMode = hasExplicitColorMode ? String(colorMode) : inheritedColorMode; `,
    `const { css: cssProp, ...boxProps } = styleProps; `,
    `const primitiveClass = '${primitiveClass}'; `,
    `const boxClass = box(boxProps as Parameters<typeof box>[0]); `,
    `const cssClass = cssProp ? css(cssProp as Parameters<typeof css>[0]) : undefined; `,
    `const classes = joinClassName(primitiveClass, boxClass, cssClass, className); `,
    `const shouldEmitDataLayer = RESOLVED_DATA_LAYER_NAME != null && RESOLVED_DATA_LAYER_NAME !== '' && (!inheritsLayerScope || hasExplicitColorMode); `,
    `const dataLayerAttr = shouldEmitDataLayer ? { 'data-layer': RESOLVED_DATA_LAYER_NAME } : {}; `,
    `const colorModeAttr = resolvedColorMode != null && resolvedColorMode !== '' ? { 'data-panda-theme': resolvedColorMode } : {}; `,
    `const providesLayerScope = inheritsLayerScope || shouldEmitDataLayer; `,
    `return <LayerScopeContext.Provider value={providesLayerScope}><ColorModeContext.Provider value={resolvedColorMode}><${tag} ref={ref} className={classes} {...dataLayerAttr} {...colorModeAttr} {...elementProps}>{children}</${tag}></ColorModeContext.Provider></LayerScopeContext.Provider>; `,
    `}); `,
    `${exportName}.displayName = '${exportName}'`,
  ].join('')
}

function generateFile(tags: string[], generatorPath: string): string {
  const importPath = join('src', 'system', 'build', 'primitives', generatorPath).split('\\').join('/')

  const lines = [
    `/** Generated by \`tsx ${importPath}\` - do not edit manually. */`,
    '',
    "import * as React from 'react'",
    "import { css } from '../runtime'",
    "import { splitCssProps } from '@reference-ui/styled/jsx'",
    "import { box } from '@reference-ui/styled/patterns/box'",
    "import { joinClassName } from './utils'",
    "import type { PrimitiveElement, PrimitiveProps } from './types'",
    '',
    "export { TAGS as HTML_TAGS, type Tag as HtmlTag } from './tags'",
    "export type { PrimitiveElement, PrimitiveProps } from './types'",
    '',
    "/** Box pattern props missing from Panda `splitCssProps` (not in is-valid-prop user list). */",
    "const BOX_PATTERN_PROPS_FOR_STYLES = ['weight'] as const",
    '',
    "function splitPrimitiveProps<T extends Record<string, unknown>>(props: T) {",
    "  const { className, children, colorMode, ...rest } = props as T & {",
    "    className?: string",
    "    children?: React.ReactNode",
    "    colorMode?: unknown",
    "  }",
    "  const [styleProps, elementProps] = splitCssProps(rest)",
    "  const domProps: Record<string, unknown> = { ...elementProps }",
    "  const patternStyle: Record<string, unknown> = {}",
    "  for (const key of BOX_PATTERN_PROPS_FOR_STYLES) {",
    "    if (key in domProps) {",
    "      patternStyle[key] = domProps[key]",
    "      delete domProps[key]",
    "    }",
    "  }",
    "  return {",
    "    className,",
    "    children,",
    "    colorMode,",
    "    styleProps: { ...styleProps, ...patternStyle },",
    "    elementProps: domProps,",
    "  }",
    "}",
    '',
    "/** Replaced at packager time with the project's ui.config.name. */",
    "const DATA_LAYER_PLACEHOLDER = ['__REFERENCE_UI', '_LAYER_NAME__'].join('')",
    "const DATA_LAYER_NAME = '__REFERENCE_UI_LAYER_NAME__' as string | undefined",
    "const RESOLVED_DATA_LAYER_NAME = DATA_LAYER_NAME === DATA_LAYER_PLACEHOLDER ? undefined : DATA_LAYER_NAME",
    "const LayerScopeContext = React.createContext(false)",
    "const ColorModeContext = React.createContext<string | undefined>(undefined)",
    '',
    ...tags.map(generatePrimitive),
    '',
    ...tags.map((tag) => `export type ${toExportName(tag)}Props = PrimitiveProps<'${tag}'>`),
    '',
  ]

  return lines.join('\n')
}

export function createPrimitiveSource(cliRoot: string): string {
  const tagsPath = join(cliRoot, 'src/system/primitives/tags.ts')
  const tags = readTags(tagsPath)
  return generateFile(tags, 'index.ts')
}
