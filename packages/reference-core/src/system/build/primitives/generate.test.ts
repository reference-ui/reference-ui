import { mkdtempSync, mkdirSync, rmSync, writeFileSync } from 'node:fs'
import { tmpdir } from 'node:os'
import { join } from 'node:path'
import { afterEach, describe, expect, it } from 'vitest'
import { createPrimitiveSource } from './generate'

const createdDirs: string[] = []

function createTempDir(): string {
  const dir = mkdtempSync(join(tmpdir(), 'reference-ui-primitives-generate-'))
  createdDirs.push(dir)
  return dir
}

function writeTagsFile(cliRoot: string, content: string): void {
  const tagsPath = join(cliRoot, 'src/system/primitives/tags.ts')
  const dir = join(cliRoot, 'src/system/primitives')
  mkdirSync(dir, { recursive: true })
  writeFileSync(tagsPath, content, 'utf-8')
}

afterEach(() => {
  for (const dir of createdDirs.splice(0)) {
    rmSync(dir, { recursive: true, force: true })
  }
})

describe('system/build/primitives/generate', () => {
  it('throws when tags.ts has no opening bracket', () => {
    const cliRoot = createTempDir()
    writeTagsFile(cliRoot, "export const TAGS = 'div' as const")

    expect(() => createPrimitiveSource(cliRoot)).toThrow(
      'Could not parse TAGS from system/primitives/tags.ts'
    )
  })

  it('throws when tags.ts has no "] as const"', () => {
    const cliRoot = createTempDir()
    writeTagsFile(cliRoot, "export const TAGS = ['div']")

    expect(() => createPrimitiveSource(cliRoot)).toThrow(
      'Could not parse TAGS from system/primitives/tags.ts'
    )
  })

  it('emits banner, DATA_LAYER_NAME, splitPrimitiveProps and ref- class for tags', () => {
    const cliRoot = createTempDir()
    writeTagsFile(cliRoot, "export const TAGS = ['div', 'span'] as const")

    const out = createPrimitiveSource(cliRoot)

    expect(out).toContain('Generated by')
    expect(out).toContain("const DATA_LAYER_NAME = '__REFERENCE_UI_LAYER_NAME__'")
    expect(out).toContain("import type { PrimitiveComponent, PrimitiveElement, PrimitiveProps } from './types'")
    expect(out).toContain('function splitPrimitiveProps')
    expect(out).toContain("primitiveClass = 'ref-div'")
    expect(out).toContain("primitiveClass = 'ref-span'")
    expect(out).toContain("export const Div: PrimitiveComponent<'div'> =")
    expect(out).toContain("export const Span: PrimitiveComponent<'span'> =")
    expect(out).toContain('export type DivProps = PrimitiveProps<\'div\'>')
    expect(out).toContain('export type SpanProps = PrimitiveProps<\'span\'>')
  })

  it('maps object -> Obj, var -> Var, single-letter a -> A', () => {
    const cliRoot = createTempDir()
    writeTagsFile(cliRoot, "export const TAGS = ['a', 'object', 'var'] as const")

    const out = createPrimitiveSource(cliRoot)

    expect(out).toContain("export const A: PrimitiveComponent<'a'> =")
    expect(out).toContain("export const Obj: PrimitiveComponent<'object'> =")
    expect(out).toContain("export const Var: PrimitiveComponent<'var'> =")
    expect(out).toContain("ref-a")
    expect(out).toContain("ref-object")
    expect(out).toContain("ref-var")
    expect(out).toContain("export type AProps = PrimitiveProps<'a'>")
    expect(out).toContain("export type ObjProps = PrimitiveProps<'object'>")
    expect(out).toContain("export type VarProps = PrimitiveProps<'var'>")
  })

  it('generates one component and one type export per tag', () => {
    const cliRoot = createTempDir()
    writeTagsFile(cliRoot, "export const TAGS = ['div', 'p'] as const")

    const out = createPrimitiveSource(cliRoot)

    const divCount = (out.match(/export const Div: PrimitiveComponent<'div'> =/g) ?? []).length
    const divPropsCount = (out.match(/export type DivProps =/g) ?? []).length
    expect(divCount).toBe(1)
    expect(divPropsCount).toBe(1)
    expect(out).toContain("export const P: PrimitiveComponent<'p'> =")
    expect(out).toContain("export type PProps = PrimitiveProps<'p'>")
  })

  it('produces identical output on rerun with same tags', () => {
    const cliRoot = createTempDir()
    writeTagsFile(cliRoot, "export const TAGS = ['div'] as const")

    const first = createPrimitiveSource(cliRoot)
    const second = createPrimitiveSource(cliRoot)

    expect(second).toBe(first)
  })
})
