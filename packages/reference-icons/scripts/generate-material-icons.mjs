/**
 * Emits one TSX component per Material Symbols slug: a single component with
 * variant "outline" | "filled", backed by *W500 / *FillW500 from
 * @material-symbols-svg/react/icons/<slug>.
 */

import { createRequire } from 'node:module'
import { mkdir, readdir, readFile, rm, writeFile } from 'node:fs/promises'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const WEIGHT = 500
const OUTLINE_SUFFIX = `W${WEIGHT}`
const FILL_SUFFIX = `FillW${WEIGHT}`

/** Join fragments with spaces (readable line builder in source). */
const sp = (...parts) => parts.filter((p) => p !== undefined && p !== '').join(' ')

const require = createRequire(import.meta.url)
const packageRoot = join(dirname(fileURLToPath(import.meta.url)), '..')
const genDir = join(packageRoot, 'src/generated')
const coreIconComponentsPath = join(
  packageRoot,
  '..',
  'reference-core',
  'src',
  'system',
  'panda',
  'config',
  'icon-components.ts'
)

const pkgJsonPath = require.resolve('@material-symbols-svg/react/package.json')
const iconsDir = join(dirname(pkgJsonPath), 'dist/icons')

const exportDeclRe = /export declare const (\w+):/g

function parseExportedNames(dts) {
  const names = new Set()
  for (const m of dts.matchAll(exportDeclRe)) {
    names.add(m[1])
  }
  return names
}

function componentNameFromOutlineSymbol(outlineSymbol) {
  if (!outlineSymbol.endsWith(OUTLINE_SUFFIX)) {
    throw new Error(`Expected outline export *${OUTLINE_SUFFIX}, got ${outlineSymbol}`)
  }
  return `${outlineSymbol.slice(0, -OUTLINE_SUFFIX.length)}Icon`
}

function pickOutlineFill(names) {
  const outline = [...names].find((n) => n.endsWith(OUTLINE_SUFFIX) && !n.endsWith(FILL_SUFFIX))
  if (!outline) return null
  const base = outline.slice(0, -OUTLINE_SUFFIX.length)
  const fillName = `${base}${FILL_SUFFIX}`
  const fill = names.has(fillName) ? fillName : outline
  return { outline, fill }
}

function emitIconFile(slug, outline, fill, componentName) {
  const mod = `@material-symbols-svg/react/icons/${slug}`
  const line1 = sp('import', '{', outline, ',', fill, '}', 'from', `'${mod}'`)
  const line2 = sp('import', '{', 'createIcon', '}', 'from', `'../createIcon'`)
  const line3 = sp('import', 'type', '{', 'MaterialSymbolIconComponent', '}', 'from', `'../types'`)
  const line4 = sp(
    'export',
    'const',
    componentName,
    ':',
    'MaterialSymbolIconComponent',
    '=',
    '/*#__PURE__*/',
    'createIcon(',
    outline,
    ',',
    fill,
    ',',
    `'${componentName}'`,
    ')',
  )
  return [line1, line2, line3, line4].join('\n')
}

function emitJsxNamesFile(componentNames) {
  return [
    '/**',
    ' * Generated by scripts/generate-material-icons.mjs — do not edit.',
    ' * Canonical JSX element names for the generated icon components.',
    ' */',
    `export const ICON_JSX_NAMES = ${JSON.stringify(componentNames, null, 2)} as const`,
    '',
    'export type IconJsxName = (typeof ICON_JSX_NAMES)[number]',
    '',
  ].join('\n')
}

function emitCoreIconComponentsFile(componentNames) {
  return [
    '/**',
    ' * Generated by packages/reference-icons/scripts/generate-material-icons.mjs — do not edit.',
    ' * Canonical JSX element names for packaged icon components.',
    ' */',
    `export const ICON_COMPONENT_NAMES = ${JSON.stringify(componentNames, null, 2)} as const`,
    '',
    'export type IconComponentName = (typeof ICON_COMPONENT_NAMES)[number]',
    '',
  ].join('\n')
}

async function main() {
  const files = await readdir(iconsDir)
  const dtsFiles = files.filter((f) => f.endsWith('.d.ts')).sort((a, b) => a.localeCompare(b))

  await rm(genDir, { recursive: true, force: true })
  await mkdir(genDir, { recursive: true })

  /** @type {Map<string, string>} */
  const exportNames = new Map()

  for (const file of dtsFiles) {
    const slug = file.replace(/\.d\.ts$/, '')
    const dts = await readFile(join(iconsDir, file), 'utf8')
    const names = parseExportedNames(dts)
    const picked = pickOutlineFill(names)
    if (!picked) continue

    const componentName = componentNameFromOutlineSymbol(picked.outline)
    if (exportNames.has(componentName)) {
      throw new Error(`Duplicate component name ${componentName}`)
    }
    exportNames.set(componentName, slug)

    const body = emitIconFile(slug, picked.outline, picked.fill, componentName)
    await writeFile(join(genDir, `${slug}.tsx`), `${body}\n`, 'utf8')
  }

  const barrel = [...exportNames.entries()]
    .sort((a, b) => a[0].localeCompare(b[0]))
    .map(([componentName, slug]) => sp('export', '{', componentName, '}', 'from', `'./${slug}'`))

  const barrelHeader = [
    '/**',
    ' * Generated by scripts/generate-material-icons.mjs — do not edit.',
    ` * Weight ${WEIGHT} outline (*${OUTLINE_SUFFIX}) and filled (*${FILL_SUFFIX}).`,
    ' */',
    '',
  ]
  await writeFile(join(genDir, 'index.ts'), `${barrelHeader.join('\n')}${barrel.join('\n')}\n`, 'utf8')
  await writeFile(
    join(packageRoot, 'src', 'jsx-names.ts'),
    emitJsxNamesFile([...exportNames.keys()].sort((a, b) => a.localeCompare(b))),
    'utf8'
  )
  await writeFile(
    coreIconComponentsPath,
    emitCoreIconComponentsFile([...exportNames.keys()].sort((a, b) => a.localeCompare(b))),
    'utf8'
  )

  console.error(`reference-icons: ${exportNames.size} icons`)
}

await main()
