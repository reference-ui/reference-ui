import { mkdirSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'
import { getConfig, getCwd } from '../../../config/store'
import { getOutDirPath } from '../../../lib/paths'
import { log } from '../../../lib/log'
import { createPortableFragmentBundle } from '../config/fragments'

export async function writeBaseSystem(): Promise<void> {
  const cwd = getCwd()
  const config = getConfig()

  if (!cwd) {
    throw new Error('writeBaseSystem: getCwd() is undefined')
  }
  if (!config) {
    throw new Error('writeBaseSystem: getConfig() is undefined')
  }

  const fragment = await createPortableFragmentBundle(cwd, config)
  const baseSystem = {
    name: config.name,
    fragment,
  }

  const systemDir = join(getOutDirPath(cwd), 'system')
  const outputPath = join(systemDir, 'baseSystem.mjs')
  const typesPath = join(systemDir, 'baseSystem.d.mts')
  mkdirSync(systemDir, { recursive: true })
  writeFileSync(
    outputPath,
    `/** Generated by reference-cli — do not edit manually */\nexport const baseSystem = ${JSON.stringify(baseSystem, null, 2)}\n`,
    'utf-8'
  )
  writeFileSync(
    typesPath,
    `import type { BaseSystem } from '@reference-ui/cli/config'\nexport declare const baseSystem: BaseSystem\n`,
    'utf-8'
  )

  log.debug('panda', 'baseSystem written', outputPath)
}
