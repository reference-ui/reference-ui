import { readFileSync } from 'node:fs'
import { join } from 'node:path'

function readTags(tagsPath: string): string[] {
  const tagsContent = readFileSync(tagsPath, 'utf8')
  const listStart = tagsContent.indexOf('[')
  const listEnd = tagsContent.indexOf('] as const', listStart)
  if (listStart < 0 || listEnd < 0) {
    throw new Error('Could not parse TAGS from system/primitives/tags.ts')
  }

  return tagsContent
    .slice(listStart + 1, listEnd)
    .split(',')
    .map((value) => value.replace(/['"]/g, '').trim())
    .filter(Boolean)
}

function toExportName(tag: string): string {
  if (tag === 'object') return 'Obj'
  if (tag === 'var') return 'Var'
  if (tag.length <= 1) return tag.toUpperCase()
  return tag.charAt(0).toUpperCase() + tag.slice(1)
}

function generatePrimitive(tag: string): string {
  const exportName = toExportName(tag)
  const primitiveClass = `ref-${tag}`

  return [
    `export const ${exportName} = React.forwardRef<PrimitiveElement<'${tag}'>, PrimitiveProps<'${tag}'>>(`,
    `(allProps, ref) => { `,
    `const typedProps = allProps as PrimitiveProps<'${tag}'> & Record<string, unknown>; `,
    `const className = typedProps.className as string | undefined; `,
    `const children = typedProps.children as React.ReactNode; `,
    `const layer = typedProps.layer as string | undefined; `,
    `const cssProp = typedProps.css; `,
    `const props = { ...typedProps }; `,
    `delete props.className; `,
    `delete props.children; `,
    `delete props.layer; `,
    `delete props.css; `,
    `const primitiveClass = '${primitiveClass}'; `,
    `const boxClass = box(props as Parameters<typeof box>[0]); `,
    `const cssClass = cssProp ? css(cssProp) : undefined; `,
    `const classes = joinClassName(primitiveClass, boxClass, cssClass, className); `,
    `const dataLayerAttr = layer != null ? { 'data-layer': layer } : {}; `,
    `return <${tag} ref={ref} className={classes} {...dataLayerAttr} {...(props as Record<string, unknown>)}>{children}</${tag}>; `,
    `}); `,
    `${exportName}.displayName = '${exportName}'`,
  ].join('')
}

function generateFile(tags: string[], generatorPath: string): string {
  const importPath = join('src', 'system', 'build', 'primitives', generatorPath).replaceAll('\\', '/')

  const lines = [
    `/** Generated by \`tsx ${importPath}\` - do not edit manually. */`,
    '',
    "import * as React from 'react'",
    "import { css } from '@reference-ui/styled/css'",
    "import { box } from '@reference-ui/styled/patterns/box'",
    "import { joinClassName } from './utils'",
    "import type { PrimitiveElement, PrimitiveProps } from './types'",
    '',
    "export { TAGS as HTML_TAGS, type Tag as HtmlTag } from './tags'",
    "export type { PrimitiveElement, PrimitiveProps } from './types'",
    '',
    ...tags.map(generatePrimitive),
    '',
    ...tags.map((tag) => `export type ${toExportName(tag)}Props = PrimitiveProps<'${tag}'>`),
    '',
  ]

  return lines.join('\n')
}

export function createPrimitiveSource(cliRoot: string): string {
  const tagsPath = join(cliRoot, 'src/system/primitives/tags.ts')
  const tags = readTags(tagsPath)
  return generateFile(tags, 'index.ts')
}
