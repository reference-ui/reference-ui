#!/usr/bin/env node
/**
 * Build script for box pattern generation.
 *
 * Collects pattern fragments from system/patterns (internal/container, internal/r)
 * and generates the box pattern file via Liquid templates.
 */

import { resolve, join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'
import { writeFileSync, mkdirSync } from 'node:fs'
import { runPatternPipeline } from '../system/patterns/index.js'

const __filename = fileURLToPath(import.meta.url)
const CLI_ROOT = resolve(dirname(__filename), '../..')
const BOX_PATTERN_OUTPUT = join(CLI_ROOT, 'src/system/internal/box.mjs')

async function buildBoxPattern(): Promise<void> {
  try {
    console.log('[build:box] Scanning for pattern fragments...')

    const { fragment, systemCount } = await runPatternPipeline({
      cwd: CLI_ROOT,
      tempDir: join(CLI_ROOT, 'src/system/internal/.tmp'),
    })

    if (systemCount === 0) {
      console.log('[build:box] No patterns found, skipping')
      return
    }

    console.log(`[build:box] Collected ${systemCount} extensions`)

    if (!fragment) {
      console.log('[build:box] No fragment to write, skipping')
      return
    }

    const content = `/** Generated by build/boxPattern.ts - do not edit manually */

${fragment}
`

    mkdirSync(dirname(BOX_PATTERN_OUTPUT), { recursive: true })
    writeFileSync(BOX_PATTERN_OUTPUT, content, 'utf-8')

    console.log(`[build:box] ✓ Generated ${BOX_PATTERN_OUTPUT}`)
    process.exit(0)
  } catch (error) {
    console.error('[build:box] ✗ Build failed:', error)
    process.exit(1)
  }
}

if (import.meta.url === `file://${process.argv[1]}`) {
  await buildBoxPattern()
}

export { buildBoxPattern }
