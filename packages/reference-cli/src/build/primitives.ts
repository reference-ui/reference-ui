#!/usr/bin/env node

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const CLI_ROOT = resolve(dirname(__filename), '../..')
const tagsPath = join(CLI_ROOT, 'src/system/primitives/tags.ts')
const outPath = join(CLI_ROOT, 'src/system/primitives/index.tsx')

function readTags(): string[] {
  const tagsContent = readFileSync(tagsPath, 'utf8')
  const listStart = tagsContent.indexOf('[')
  const listEnd = tagsContent.indexOf('] as const', listStart)
  if (listStart < 0 || listEnd < 0) {
    throw new Error('Could not parse TAGS from system/primitives/tags.ts')
  }

  return tagsContent
    .slice(listStart + 1, listEnd)
    .split(',')
    .map((value) => value.replace(/['"]/g, '').trim())
    .filter(Boolean)
}

function toExportName(tag: string): string {
  if (tag === 'object') return 'Obj'
  if (tag === 'var') return 'Var'
  if (tag.length <= 1) return tag.toUpperCase()
  return tag.charAt(0).toUpperCase() + tag.slice(1)
}

function generatePrimitive(tag: string): string {
  const exportName = toExportName(tag)
  const primitiveClass = `ref-${tag}`

  return `export const ${exportName} = React.forwardRef<PrimitiveElement<'${tag}'>, PrimitiveProps<'${tag}'>>(
  ({ className, children, layer, ...props }, ref) => {
    const primitiveClass = '${primitiveClass}'
    const boxClass = box(props)
    const classes = joinClassName(primitiveClass, boxClass, className)
    const dataLayerAttr = layer != null ? { 'data-layer': layer } : {}
    return <${tag} ref={ref} className={classes} {...dataLayerAttr} {...(props as Record<string, unknown>)}>{children}</${tag}>
  }
)

${exportName}.displayName = '${exportName}'`
}

function generateFile(tags: string[]): string {
  const lines = [
    '/** Generated by `tsx src/build/primitives.ts` - do not edit manually. */',
    '',
    "import * as React from 'react'",
    "import { box } from '@reference-ui/styled/patterns/box'",
    "import { TAGS } from './tags'",
    "import type { PrimitiveElement, PrimitiveProps } from './types'",
    '',
    "export { TAGS as HTML_TAGS, type Tag as HtmlTag } from './tags'",
    "export type { PrimitiveElement, PrimitiveProps } from './types'",
    '',
    'function joinClassName(...parts: Array<string | undefined>): string | undefined {',
    '  const className = parts.filter(Boolean).join(\' \').trim()',
    '  return className || undefined',
    '}',
    '',
    ...tags.map(generatePrimitive),
    '',
    ...tags.map((tag) => `export type ${toExportName(tag)}Props = PrimitiveProps<'${tag}'>`),
    '',
    'void TAGS',
    '',
  ]

  return lines.join('\n')
}

export async function buildPrimitives(): Promise<void> {
  const tags = readTags()
  const content = generateFile(tags)
  writeFileSync(outPath, content, 'utf8')
  console.log(`[build:primitives] Generated ${outPath}`)
}

if (import.meta.url === `file://${process.argv[1]}`) {
  await buildPrimitives()
}
