use std::fs;
use std::path::{Path, PathBuf};

use crate::tasty::constants::scanner::NODE_MODULES_DIR;

pub(super) fn candidate_node_modules_dirs(root_dir: &Path) -> Vec<PathBuf> {
    root_dir
        .ancestors()
        .map(|ancestor| ancestor.join(NODE_MODULES_DIR))
        .filter(|path| path.is_dir())
        .collect()
}

pub(super) fn installed_package_dirs(root_dir: &Path) -> Vec<PathBuf> {
    let mut package_dirs = Vec::new();
    for node_modules_dir in candidate_node_modules_dirs(root_dir) {
        let Ok(entries) = fs::read_dir(node_modules_dir) else {
            continue;
        };

        for entry in entries.flatten() {
            package_dirs.extend(package_dirs_for_entry(entry.path()));
        }
    }

    package_dirs
}

fn package_dirs_for_entry(path: PathBuf) -> Vec<PathBuf> {
    if !path.is_dir() {
        return Vec::new();
    }

    let Some(name) = path.file_name().and_then(|value| value.to_str()) else {
        return Vec::new();
    };

    if name.starts_with('@') {
        return scoped_package_dirs(&path);
    }

    vec![path]
}

fn scoped_package_dirs(scope_dir: &Path) -> Vec<PathBuf> {
    let Ok(entries) = fs::read_dir(scope_dir) else {
        return Vec::new();
    };

    entries
        .flatten()
        .map(|entry| entry.path())
        .filter(|path| path.is_dir())
        .collect()
}
